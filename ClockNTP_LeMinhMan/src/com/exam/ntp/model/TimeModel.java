package com.exam.ntp.model;

/**
 * 
 * @author manle
 *
 */
public class TimeModel {

	private int second;
	private int minute;
	private int hour;
	
	public int getSecond() {
		return second;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	public int getMinute() {
		return minute;
	}
	public void setMinute(int minute) {
		this.minute = minute;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	
	
}
